namespace PanoramicData.Blazor.Enums;

/// <summary>
/// Defines the vertical content alignment for widget content.
/// </summary>
public enum ContentAlignment
{
	/// <summary>
	/// Content is aligned to the top. Default.
	/// </summary>
	Top,

	/// <summary>
	/// Content is vertically centered.
	/// </summary>
	Center,

	/// <summary>
	/// Content is aligned to the bottom.
	/// </summary>
	Bottom
}
